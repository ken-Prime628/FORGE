@Composable
private fun PortfolioTabSwitcher(activeTab: PortfolioTab, onSelect: (PortfolioTab) -> Unit) {
    val tabs = listOf(PortfolioTab.WORKS to "Works", PortfolioTab.BUILDER to "Builder", PortfolioTab.PREVIEW to "Preview")
    Row(
        modifier = Modifier.fillMaxWidth().padding(horizontal = 16.dp, vertical = 12.dp)
            .clip(RoundedCornerShape(14.dp)).background(BackgroundSecondary)
    ) {
        tabs.forEach { (tab, label) ->
            val active = activeTab == tab
            Box(
                modifier = Modifier.weight(1f).clip(RoundedCornerShape(12.dp))
                    .background(if (active) Brush.horizontalGradient(listOf(GoldPrimary, GoldAccent))
                    else Brush.horizontalGradient(listOf(Color.Transparent, Color.Transparent)))
                    .clickable { onSelect(tab) }.padding(vertical = 10.dp),
                contentAlignment = Alignment.Center
            ) {
                Text(label,
                    color = if (active) DarkSurface else TextSecondary,
                    fontWeight = if (active) FontWeight.Bold else FontWeight.Normal,
                    fontSize = 13.sp)
            }
        }
    }
}

