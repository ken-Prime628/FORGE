data class PortfolioItem(
    val id: String            = "",
    val title: String         = "",
    val category: String      = "",
    val description: String   = "",
    val imageUrl: String       = "",   // Cloudinary URL or empty
    val tags: List<String>    = emptyList(),
    val featured: Boolean     = false,
    val createdAt: Long       = System.currentTimeMillis()
)

data class PortfolioMeta(
    val uid: String           = "",
    val headline: String      = "",   // e.g. "Creative Designer & Storyteller"
    val tagline: String       = "",   // 1-liner used in pitch
    val theme: String         = "dark",
    val isPublished: Boolean  = false,
    val autoGenerated: Boolean = false,
    val updatedAt: Long       = System.currentTimeMillis()
)

enum class PortfolioTab { WORKS, BUILDER, PREVIEW }

// ─────────────────────────────────────────────────────────────────
//  FIREBASE HELPERS
// ─────────────────────────────────────────────────────────────────

private suspend fun loadPortfolioMeta(uid: String): PortfolioMeta? =
    try {
        val snap = Firebase.database.reference.child("users").child(uid)
            .child("portfolio").child("meta").get().await()
        snap.getValue(PortfolioMeta::class.java)
    } catch (e: Exception) { null }

private suspend fun loadPortfolioItems(uid: String): List<PortfolioItem> =
    try {
        val snap = Firebase.database.reference.child("users").child(uid)
            .child("portfolio").child("items").get().await()
        snap.children.mapNotNull { it.getValue(PortfolioItem::class.java) }
            .sortedByDescending { it.createdAt }
    } catch (e: Exception) { emptyList() }

private suspend fun savePortfolioMeta(uid: String, meta: PortfolioMeta): Result<Unit> =
    runCatching {
        Firebase.database.reference.child("users").child(uid)
            .child("portfolio").child("meta").setValue(meta).await()
    }

private suspend fun savePortfolioItem(uid: String, item: PortfolioItem): Result<Unit> =
    runCatching {
        val ref = if (item.id.isEmpty())
            Firebase.database.reference.child("users").child(uid).child("portfolio").child("items").push()
        else
            Firebase.database.reference.child("users").child(uid).child("portfolio").child("items").child(item.id)
        ref.setValue(item.copy(id = ref.key ?: item.id)).await()
    }

private suspend fun deletePortfolioItem(uid: String, itemId: String): Result<Unit> =
    runCatching {
        Firebase.database.reference.child("users").child(uid)
            .child("portfolio").child("items").child(itemId).removeValue().await()
    }

private suspend fun toggleFeatured(uid: String, item: PortfolioItem): Result<Unit> =
    runCatching {
        Firebase.database.reference.child("users").child(uid)
            .child("portfolio").child("items").child(item.id)
            .child("featured").setValue(!item.featured).await()
    }

/** Reads the user profile and uploads, then builds a PortfolioMeta automatically */
private suspend fun autoGeneratePortfolio(uid: String): Result<PortfolioMeta> = runCatching {
    val profileSnap = Firebase.database.reference.child("users").child(uid)
        .child("profile").get().await()
    val name       = profileSnap.child("name").getValue(String::class.java) ?: "Creator"
    val profession = profileSnap.child("profession").getValue(String::class.java) ?: "Creative"
    val bio        = profileSnap.child("bio").getValue(String::class.java) ?: ""

    val generated = PortfolioMeta(
        uid           = uid,
        headline      = "$profession · Visual Storyteller · Problem Solver",
        tagline       = if (bio.isNotBlank()) bio.take(120) else "Crafting bold ideas into unforgettable experiences.",
        theme         = "dark",
        isPublished   = false,
        autoGenerated = true
    )
    savePortfolioMeta(uid, generated).getOrThrow()
    generated
}

