@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun PortfolioBuilderScreen(navController: NavController) {

    val isPreview = LocalInspectionMode.current
    val scope     = rememberCoroutineScope()
    val uid       = if (isPreview) "preview_uid" else Firebase.auth.currentUser?.uid ?: ""

    var activeTab       by remember { mutableStateOf(PortfolioTab.BUILDER) }
    var meta            by remember { mutableStateOf(PortfolioMeta()) }
    var items           by remember { mutableStateOf<List<PortfolioItem>>(emptyList()) }
    var isLoading       by remember { mutableStateOf(!isPreview) }
    var isSaving        by remember { mutableStateOf(false) }
    var isGenerating    by remember { mutableStateOf(false) }
    var errorMsg        by remember { mutableStateOf("") }
    var showAddDialog   by remember { mutableStateOf(false) }
    var editingItem     by remember { mutableStateOf<PortfolioItem?>(null) }
    var showDeleteId    by remember { mutableStateOf("") }

    // ── Preview mock data ─────────────────────────────────────────
    LaunchedEffect(Unit) {
        if (isPreview) {
            meta  = PortfolioMeta(uid = "preview", headline = "Creative Designer & Visual Storyteller",
                tagline = "Crafting bold ideas into unforgettable experiences.", isPublished = true)
            items = listOf(
                PortfolioItem("1", "Brand Identity – Forge", "Branding", "Full visual identity system", featured = true),
                PortfolioItem("2", "Motion UI Kit",          "Motion",   "Micro-interaction library",  featured = false),
                PortfolioItem("3", "Editorial Layout",       "Print",    "Magazine spread design",     featured = true)
            )
            isLoading = false
            return@LaunchedEffect
        }
        if (uid.isEmpty()) { isLoading = false; return@LaunchedEffect }
        val loadedMeta  = loadPortfolioMeta(uid)
        val loadedItems = loadPortfolioItems(uid)
        meta    = loadedMeta ?: PortfolioMeta(uid = uid)
        items   = loadedItems
        isLoading = false
    }

    Scaffold(
        containerColor = BackgroundMain,
        topBar = {
            CenterAlignedTopAppBar(
                title = {
                    Text("Portfolio Builder", fontWeight = FontWeight.Black,
                        letterSpacing = 0.5.sp, color = TextPrimary)
                },
                navigationIcon = {
                    IconButton(onClick = { navController.popBackStack() }) {
                        Icon(Icons.AutoMirrored.Filled.ArrowBack, null, tint = TextPrimary)
                    }
                },
                actions = {
                    // Publish toggle
                    AnimatedContent(targetState = meta.isPublished, label = "pub") { published ->
                        Box(
                            modifier = Modifier
                                .clip(RoundedCornerShape(10.dp))
                                .background(if (published) SoftGreen.copy(0.15f) else BackgroundSecondary)
                                .clickable {
                                    val updated = meta.copy(isPublished = !meta.isPublished)
                                    meta = updated
                                    if (uid.isNotEmpty()) scope.launch { savePortfolioMeta(uid, updated) }
                                }
                                .padding(horizontal = 10.dp, vertical = 6.dp)
                        ) {
                            Text(
                                if (published) "Published" else "Draft",
                                color      = if (published) SoftGreen else TextSecondary,
                                fontSize   = 11.sp,
                                fontWeight = FontWeight.Bold
                            )
                        }
                    }
                    Spacer(Modifier.width(8.dp))
                },
                colors = TopAppBarDefaults.centerAlignedTopAppBarColors(containerColor = BackgroundMain)
            )
        }
    ) { padding ->

        if (isLoading) {
            Box(Modifier.fillMaxSize().padding(padding), contentAlignment = Alignment.Center) {
                CircularProgressIndicator(color = GoldPrimary)
            }
            return@Scaffold
        }

        Column(modifier = Modifier.fillMaxSize().padding(padding)) {

            // ── TAB SWITCHER ──────────────────────────────────────
            PortfolioTabSwitcher(activeTab = activeTab, onSelect = { activeTab = it })

            // ── CONTENT ───────────────────────────────────────────
            Column(
                modifier = Modifier
                    .fillMaxSize()
                    .verticalScroll(rememberScrollState())
                    .padding(bottom = 32.dp)
            ) {
                AnimatedContent(
                    targetState   = activeTab,
                    transitionSpec = { fadeIn(tween(200)) togetherWith fadeOut(tween(200)) },
                    label         = "tab"
                ) { tab ->
                    when (tab) {

                        // ── WORKS TAB ─────────────────────────────
                        PortfolioTab.WORKS -> WorksTabContent(
                            items        = items,
                            onAddClick   = { showAddDialog = true; editingItem = null },
                            onEditClick  = { editingItem = it; showAddDialog = true },
                            onDeleteClick = { showDeleteId = it.id },
                            onFeatureClick = { item ->
                                scope.launch {
                                    toggleFeatured(uid, item)
                                    items = items.map { if (it.id == item.id) it.copy(featured = !it.featured) else it }
                                }
                            }
                        )

                        // ── BUILDER TAB ───────────────────────────
                        PortfolioTab.BUILDER -> BuilderTabContent(
                            meta          = meta,
                            isGenerating  = isGenerating,
                            isSaving      = isSaving,
                            errorMsg      = errorMsg,
                            onMetaChange  = { meta = it },
                            onSave        = {
                                isSaving = true; errorMsg = ""
                                scope.launch {
                                    savePortfolioMeta(uid, meta).fold(
                                        onSuccess = { withContext(Dispatchers.Main) { isSaving = false } },
                                        onFailure = { e -> withContext(Dispatchers.Main) { isSaving = false; errorMsg = e.message ?: "Save failed" } }
                                    )
                                }
                            },
                            onAutoGenerate = {
                                isGenerating = true; errorMsg = ""
                                scope.launch {
                                    autoGeneratePortfolio(uid).fold(
                                        onSuccess = { generated -> withContext(Dispatchers.Main) { meta = generated; isGenerating = false } },
                                        onFailure = { e -> withContext(Dispatchers.Main) { isGenerating = false; errorMsg = e.message ?: "Generation failed" } }
                                    )
                                }
                            }
                        )

                        // ── PREVIEW TAB ───────────────────────────
                        PortfolioTab.PREVIEW -> PreviewTabContent(
                            meta       = meta,
                            items      = items,
                            onPitchClick = { navController.navigate("pitch_view") }
                        )
                    }
                }
            }
        }

        // ── ADD / EDIT DIALOG ─────────────────────────────────────
        if (showAddDialog) {
            AddEditItemDialog(
                initial    = editingItem,
                onDismiss  = { showAddDialog = false; editingItem = null },
                onSave     = { item ->
                    showAddDialog = false
                    scope.launch {
                        savePortfolioItem(uid, item).fold(
                            onSuccess = {
                                val refreshed = loadPortfolioItems(uid)
                                withContext(Dispatchers.Main) { items = refreshed }
                            },
                            onFailure = { e ->
                                withContext(Dispatchers.Main) { errorMsg = e.message ?: "Save failed" }
                            }
                        )
                    }
                }
            )
        }

        // ── DELETE CONFIRMATION ───────────────────────────────────
        if (showDeleteId.isNotEmpty()) {
            AlertDialog(
                onDismissRequest  = { showDeleteId = "" },
                title             = { Text("Remove work?", fontWeight = FontWeight.Bold) },
                text              = { Text("This will permanently remove this item from your portfolio.") },
                confirmButton     = {
                    TextButton(onClick = {
                        val id = showDeleteId; showDeleteId = ""
                        scope.launch {
                            deletePortfolioItem(uid, id)
                            items = items.filter { it.id != id }
                        }
                    }) { Text("Remove", color = Error, fontWeight = FontWeight.Bold) }
                },
                dismissButton     = {
                    TextButton(onClick = { showDeleteId = "" }) { Text("Cancel", color = TextSecondary) }
                },
                containerColor    = CardBackground
            )
        }
    }
}

