@Composable
private fun BuilderTabContent(
    meta: PortfolioMeta,
    isGenerating: Boolean,
    isSaving: Boolean,
    errorMsg: String,
    onMetaChange: (PortfolioMeta) -> Unit,
    onSave: () -> Unit,
    onAutoGenerate: () -> Unit
) {
    Column(modifier = Modifier.padding(horizontal = 16.dp)) {

        // Auto-generate card
        AutoGenerateCard(isGenerating = isGenerating, onGenerate = onAutoGenerate)

        Spacer(Modifier.height(20.dp))

        // Section label
        Row(verticalAlignment = Alignment.CenterVertically, modifier = Modifier.padding(bottom = 14.dp)) {
            Box(Modifier.width(3.dp).height(16.dp).background(GoldGradient, RoundedCornerShape(2.dp)))
            Spacer(Modifier.width(10.dp))
            Text("CUSTOMISE YOUR PORTFOLIO", color = TextSecondary, fontSize = 10.sp,
                fontWeight = FontWeight.Bold, letterSpacing = 2.sp)
        }

        Card(
            modifier  = Modifier.fillMaxWidth(),
            shape     = RoundedCornerShape(20.dp),
            colors    = CardDefaults.cardColors(containerColor = CardBackground),
            elevation = CardDefaults.cardElevation(2.dp)
        ) {
            Column(modifier = Modifier.padding(20.dp), verticalArrangement = Arrangement.spacedBy(16.dp)) {

                // Headline
                BuilderField(
                    label = "Professional Headline",
                    value = meta.headline,
                    hint  = "e.g. Designer · Visual Storyteller · Problem Solver",
                    onValueChange = { onMetaChange(meta.copy(headline = it)) }
                )

                HorizontalDivider(color = BackgroundSecondary, thickness = 0.5.dp)

                // Tagline
                BuilderField(
                    label      = "Tagline / Pitch Summary",
                    value      = meta.tagline,
                    hint       = "1–2 sentences that capture who you are and what you create",
                    singleLine = false,
                    minLines   = 3,
                    onValueChange = { onMetaChange(meta.copy(tagline = it)) }
                )

                HorizontalDivider(color = BackgroundSecondary, thickness = 0.5.dp)

                // Theme selector
                Column {
                    Text("Portfolio Theme", color = TextSecondary, fontSize = 11.sp,
                        fontWeight = FontWeight.Bold, letterSpacing = 0.5.sp)
                    Spacer(Modifier.height(10.dp))
                    Row(horizontalArrangement = Arrangement.spacedBy(10.dp)) {
                        listOf("dark" to "Dark", "light" to "Light", "gold" to "Gold").forEach { (value, label) ->
                            ThemeChip(
                                label     = label,
                                isSelected = meta.theme == value,
                                color     = when (value) {
                                    "dark"  -> DarkSurface
                                    "light" -> BackgroundMain
                                    else    -> GoldPrimary
                                },
                                onClick   = { onMetaChange(meta.copy(theme = value)) }
                            )
                        }
                    }
                }
            }
        }

        // Error
        AnimatedVisibility(visible = errorMsg.isNotEmpty(), enter = fadeIn(), exit = fadeOut()) {
            Spacer(Modifier.height(12.dp))
            Row(
                modifier = Modifier.fillMaxWidth().clip(RoundedCornerShape(10.dp))
                    .background(Error.copy(0.08f)).padding(12.dp),
                verticalAlignment = Alignment.CenterVertically
            ) {
